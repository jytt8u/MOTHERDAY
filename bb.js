const wishesContainer = document.getElementById('wishesContainer');

// Функция для отображения пожеланий одно за другим
function displayWishes(index) {
    if (index < wishes.length) {
        const wishElement = document.createElement('p');
        wishElement.classList.add('wish');
        wishElement.innerText = wishes[index];
        wishesContainer.appendChild(wishElement);

        // Устанавливаем задержку для следующего пожелания
        setTimeout(() => displayWishes(index + 1), 1500);
    }
}

// Запускаем отображение пожеланий
displayWishes(0);

// Функция для генерации сердечек
function createHeart() {
    const heart = document.createElement('div');
    heart.classList.add('heart');
    heart.style.left = Math.random() * 100 + 'vw';
    heart.style.animationDuration = Math.random() * 2 + 3 + 's';
    document.getElementById('heartsContainer').appendChild(heart);

    setTimeout(() => {
        heart.remove();
    }, 5000);
}

// Генерация сердечек каждые 300 мс
setInterval(createHeart, 300);